public class Seventh {
    public static void main(String[] args){

        System.out.println(Math.PI);
        System.out.println(Math.E);

        double number;

        number = Math.pow(2,3);
        number = Math.abs(-34);
        number = Math.sqrt(64);
        number = Math.round(3.14);
        number = Math.ceil(3.14);   // Rounds the number Up to Ceiling, meaning the next number
        number = Math.floor(3.99);    // Rounds the number Down to Floor, meaning the base number
        number = Math.max(23,19);
        number = Math.min(34,42);

        System.out.println(number);
    }
}
